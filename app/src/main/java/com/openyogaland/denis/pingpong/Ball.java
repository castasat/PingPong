package com.openyogaland.denis.pingpong;

// this class represents a ping-pong ball
public class Ball
{
  // fields
  private int x;
  private int y;
  private int radius;
  private int color;
  
  // setter setColor()
  public void setColor(int color)
  {
    this.color = color;
  }
  
  // getter getX()
  public int getX()
  {
    return x;
  }
  // getter getY()
  public int getY()
  {
    return y;
  }
  // getter getRadius()
  public int getRadius()
  {
    return radius;
  }
  // getter getColor()
  public int getColor()
  {
    return color;
  }

}
